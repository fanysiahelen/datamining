"""## 4. Modeling

In this phase, we will apply the K-Means clustering algorithm to group the provinces based on their educational participation rates. The first step is to determine the optimal number of clusters (k).

### Elbow Method to Determine Optimal k

The Elbow Method is a heuristic used to determine the number of clusters in a dataset. The idea is to run K-Means clustering for a range of k values (e.g., from 1 to 10) and calculate the sum of squared distances from each point to its assigned center (inertia). The plot of inertia against the number of clusters should resemble an arm, and the 'elbow' (the point of inflection) on the curve indicates the optimal k.
"""

from sklearn.cluster import KMeans
import warnings
warnings.filterwarnings('ignore') # Suppress KMeans convergence warnings

# Calculate inertia for different numbers of clusters
inertia = []
for k in range(1, 11):
    kmeans = KMeans(n_clusters=k, random_state=42, n_init=10) # Added n_init to suppress warning
    kmeans.fit(X_scaled)
    inertia.append(kmeans.inertia_)

# Plot the Elbow Method graph
plt.figure(figsize=(10, 6))
plt.plot(range(1, 11), inertia, marker='o', linestyle='--')
plt.title('Elbow Method for Optimal K')
plt.xlabel('Number of Clusters (K)')
plt.ylabel('Inertia (Within-cluster sum of squares)')
plt.xticks(range(1, 11))
plt.grid(True)
plt.show()

"""### Applying K-Means Clustering

Based on the Elbow Method, we will choose `k=3` as the optimal number of clusters. We will now apply the K-Means algorithm with this chosen k and assign the cluster labels back to our original DataFrame.
"""

# Initialize KMeans with the optimal number of clusters (k=3)
k_optimal = 3
kmeans_model = KMeans(n_clusters=k_optimal, random_state=42, n_init=10)

# Fit KMeans to the scaled data and predict clusters
df['Cluster'] = kmeans_model.fit_predict(X_scaled)

# Display the first few rows with the assigned clusters
display(df.head())

"""### Cluster Analysis

Now that the provinces have been assigned to clusters, we need to analyze the characteristics of each cluster to understand what differentiates them. We will calculate the mean `APM_SD`, `APM_SMP`, and `APM_SMA` for each cluster and count the number of provinces in each cluster.
"""

# Calculate the mean of APM values for each cluster
cluster_means = df.groupby('Cluster')[['APM_SD', 'APM_SMP', 'APM_SMA']].mean()
display(cluster_means)

# Count the number of provinces in each cluster
cluster_counts = df['Cluster'].value_counts().sort_index()
print("\nNumber of provinces per cluster:")
display(cluster_counts)

