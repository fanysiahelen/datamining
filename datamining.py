"""## 5. Evaluation

In this phase, we will evaluate the quality of the clustering. While K-Means does not have a 'ground truth' to compare against, we can use intrinsic evaluation metrics to assess how well-separated and compact our clusters are. We will use the Silhouette Score, which measures how similar an object is to its own cluster compared to other clusters.

### Silhouette Score

The Silhouette Score is a measure of how similar an object is to its own cluster (cohesion) compared to other clusters (separation). The silhouette ranges from -1 to +1, where a high value indicates that the object is well matched to its own cluster and poorly matched to neighboring clusters.
"""

from sklearn.metrics import silhouette_score

# Calculate the Silhouette Score for the clustering
silhouette_avg = silhouette_score(X_scaled, df['Cluster'])

print(f"The average Silhouette Score for the clustering is: {silhouette_avg:.2f}")

